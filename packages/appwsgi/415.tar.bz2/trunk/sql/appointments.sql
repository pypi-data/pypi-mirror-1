DROP TABLE IF EXISTS appointments;

CREATE TABLE IF NOT EXISTS appointments (
    uid         BIGINT UNSIGNED,
    aid         SERIAL PRIMARY KEY,
    calendar    DATETIME,
    appointment VARCHAR(1024),
    FOREIGN KEY(uid) REFERENCES register(uid) ON UPDATE CASCADE ON DELETE CASCADE
) ENGINE=InnoDB;

